package day05ifstatement;

public class IfStatement01 {
    
   

    public static void main(String[] args) {
        int a =3;
        int b=3; 
        if (a>=b){
            System.out.println(a+b);

        }

        if (a==b){
            System.out.println(a*b);

        }

        if (a<=b){
            System.out.println(a-b);

        }

        
        
        
        
        
        
    }// main

    
    
}// class
